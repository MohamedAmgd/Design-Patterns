
public class Item {

    String name;
    String sku;
    float price;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }

    public String getSKU() {
        return this.sku;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public float getPrice() {
        return price;
    }

}
